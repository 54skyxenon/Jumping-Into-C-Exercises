#include <iostream>
using namespace std;

#include "Operators.h"

int add(int term1, int term2)
{
    return term1 + term2;
}

int subtract(int term1, int term2)
{
    return term1 - term2;
}

int divide(int term1, int term2)
{
    return term1 / term2;
}

int multiply(int term1, int term2)
{
    return term1 * term2;
}
